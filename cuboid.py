def areacuboid():
	l=int(input("enter the length:"))
	b=int(input("enter the breadth:"))
	h=int(input("enter the height:"))
	print("total surface area of cuboid is",2*(l*b+b*h+l*h))
def percuboid():
	l=int(input("enter the length:"))
	b=int(input("enter the breadth:"))
	h=int(input("enter the height:"))
	print("perimeter of cuboid is",4*(l+b+h))

